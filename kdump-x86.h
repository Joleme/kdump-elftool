#ifndef KDUMP_X86_H
#define KDUMP_X86_H

/*
 * Export some specific things so that qemumem can use it.
 */

struct x86_64_data
{
	bool dummy;
};

struct x86_64_pt_regs {
	uint64_t r15;
	uint64_t r14;
	uint64_t r13;
	uint64_t r12;
	uint64_t rbp;
	uint64_t rbx;
	uint64_t r11;
	uint64_t r10;
	uint64_t r9;
	uint64_t r8;
	uint64_t rax;
	uint64_t rcx;
	uint64_t rdx;
	uint64_t rsi;
	uint64_t rdi;
	uint64_t orig_rax;
	uint64_t rip;
	uint64_t cs;
	uint64_t eflags;
	uint64_t rsp;
	uint64_t ss;
	uint64_t fs_base;
	uint64_t gs_base;
	uint64_t ds;
	uint64_t es;
	uint64_t fs;
	uint64_t gs;
	/* I'm not sure what the 8 bytes at the end is, but it's required. */
	uint64_t dummy;
};

struct i386_data
{
	bool pae;
};

struct i386_pt_regs {
	uint32_t ebx;
	uint32_t ecx;
	uint32_t edx;
	uint32_t esi;
	uint32_t edi;
	uint32_t ebp;
	uint32_t eax;
	uint32_t xds;
	uint32_t xes;
	uint32_t xfs;
	uint32_t xgs;
	uint32_t orig_eax;
	uint32_t eip;
	uint32_t xcs;
	uint32_t eflags;
	uint32_t esp;
	uint32_t xss;
	/* I'm not sure what the 4 bytes at the end is, but it's required. */
	uint32_t dummy;
};

#endif /* KDUMP_X86_H */
